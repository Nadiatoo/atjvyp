import pandas as pd
import numpy as np
import json
from datetime import datetime

class ReportGenerator:
    def __init__(self, data_dir="zhuangjiaren_backtest"):
        self.data_dir = data_dir
        self.records = None
        self.stats = None
        self.load_data()
    
    def load_data(self):
        """加载回测数据"""
        try:
            self.records = pd.read_csv(f"{self.data_dir}/reports/backtest_records.csv")
            with open(f"{self.data_dir}/reports/backtest_stats.json", 'r') as f:
                self.stats = json.load(f)
            print(f"✓ 加载回测数据: {len(self.records)}条")
        except Exception as e:
            print(f"✗ 加载数据失败: {e}")
    
    def analyze_failure_cases(self):
        """详细分析失效案例"""
        df = self.records
        cases = []
        
        # 1. 假春播案例（判断为春播，但后续大跌）
        spring_failures = df[(df['season_judgment'] == '春播') & (df['return_5d'] < -2)]
        for _, row in spring_failures.iterrows():
            cases.append({
                '日期': row['date'],
                '类型': '假春播',
                '判断': '春播（恐慌后布局）',
                '实际结果': f"5日跌幅{row['return_5d']}%",
                '20日结果': f"{row['return_20d']}%",
                '原因分析': '恐慌后出现，但强势股补跌未完成或整体趋势仍向下',
                '改进建议': '增加强势股补跌确认条件，要求跌破前期低点或出现放量恐慌'
            })
        
        # 2. 错过春播（大跌后没判断为春播，但后续大涨）
        missed_spring = df[(df['season_judgment'] != '春播') & 
                           (df['return_20d'] > 8) & 
                           (df['index_change_pct'] < -1)]
        for _, row in missed_spring.head(5).iterrows():
            cases.append({
                '日期': row['date'],
                '类型': '错过春播',
                '判断': row['season_judgment'],
                '实际结果': f"20日涨幅{row['return_20d']}%",
                '当日跌幅': f"{row['index_change_pct']}%",
                '原因分析': '大跌当日未被识别为恐慌，可能是缺乏其他确认信号',
                '改进建议': '降低恐慌识别阈值，或增加尾盘/次日确认机制'
            })
        
        # 3. 假秋收案例（判断为秋收，但后续继续大涨）
        autumn_failures = df[(df['season_judgment'] == '秋收') & (df['return_10d'] > 5)]
        for _, row in autumn_failures.iterrows():
            cases.append({
                '日期': row['date'],
                '类型': '假秋收',
                '判断': '秋收（高潮兑现）',
                '实际结果': f"10日涨幅{row['return_10d']}%",
                '20日结果': f"{row['return_20d']}%",
                '原因分析': '主升浪中的调整被误判为秋收，实际还有次高潮',
                '改进建议': '秋收判断需结合板块强度，单次高潮不轻易判断为秋收'
            })
        
        # 4. 错过秋收（大涨后没判断为秋收，但后续大跌）
        missed_autumn = df[(df['season_judgment'] != '秋收') & 
                           (df['return_20d'] < -5) & 
                           (df['return_5d'] > 3)]
        for _, row in missed_autumn.head(5).iterrows():
            cases.append({
                '日期': row['date'],
                '类型': '错过秋收',
                '判断': row['season_judgment'],
                '前5日涨幅': f"{row['return_5d']}%",
                '实际结果': f"20日跌幅{row['return_20d']}%",
                '原因分析': '大涨后未被识别为高潮，错过最佳离场点',
                '改进建议': '增加偏离度阈值，连续上涨后提高警惕'
            })
        
        # 5. 夏长判断失误案例
        summer_failures = df[(df['season_judgment'] == '夏长') & 
                            (df['return_10d'] < -3)]
        for _, row in summer_failures.head(3).iterrows():
            cases.append({
                '日期': row['date'],
                '类型': '夏长误判',
                '判断': '夏长（持股）',
                '实际结果': f"10日跌幅{row['return_10d']}%",
                '原因分析': '趋势已被破坏但仍判断为夏长，未能及时转入冬藏',
                '改进建议': '夏长判断增加止损条件，跌破20日线自动转为冬藏'
            })
        
        return pd.DataFrame(cases)
    
